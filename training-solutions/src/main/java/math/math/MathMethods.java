package math.math;

public class MathMethods {
    public static void main(String[] args) {
        System.out.println(Math.max(2, 4));
        System.out.println(Math.min(2, 4));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(3.14));
        System.out.println(Math.abs(-5));
        System.out.println(Math.negateExact(1));
        System.out.println(Math.addExact(50, 50));
        System.out.println(Math.subtractExact(100, 50));
        System.out.println(Math.multiplyExact(5, 5));
        System.out.println(Math.pow(5, 5));
        System.out.println(Math.incrementExact(5));
        System.out.println(Math.decrementExact(5));
        System.out.println(Math.random());
    }
}
